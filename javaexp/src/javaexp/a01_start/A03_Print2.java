package javaexp.a01_start;

public class A03_Print2{

	public static void main(String[] args) {
		//ex2)java의 출력형식을 이용해서 학생 3명의 성적 출려해보기
		
		System.out.println("# 학생 3명의 성적 #");
		System.out.println("철수 : 100점");
		System.out.println("유리 : 80점");
		System.out.println("짱구 : 60점");
		
		System.out.println("\n# 학생 3명의 성적 #");
		System.out.println("순서\t이름\t점수");
		System.out.println("1\t철수\t100점");
		System.out.println("2\t유리\t80점");
		System.out.println("3\t짱구\t60점");
		

	}

}
